SET 연산자
1. UNION : 합집합(중복제외) >>컬럼수가 같고, 비교순서 데이터 타입이 같아야한다.
2. UNION ALL : 합집합(중복)
3. INTERSECT : 교집합
4. MINUS : 차집합


UNION은 기본적으로 정렬을 한다.(조건 : SELECT문의 첫번째 컬럼값을 오름차순 한 것을 기준으로)

UNION ALL  : SORT가 되어있지 않다. 정렬X, DISTINCT 사용불가

SELECT EMPLOYEE_ID, JOB_ID
FROM EMPLOYEES_EX
UNION
SELECT EMPLOYEE_ID, JOB_ID
FROM JOB_HISTORY;

--

SELECT EMPLOYEE_ID, JOB_ID
FROM EMPLOYEES_EX
UNION ALL
SELECT EMPLOYEE_ID, JOB_ID
FROM JOB_HISTORY
ORDER BY EMPLOYEE_ID;

--

SELECT EMPLOYEE_ID, JOB_ID
FROM EMPLOYEES_EX
INTERSECT
SELECT EMPLOYEE_ID, JOB_ID
FROM JOB_HISTORY;

--

SELECT EMPLOYEE_ID, JOB_ID
FROM EMPLOYEES_EX
MINUS
SELECT EMPLOYEE_ID, JOB_ID
FROM JOB_HISTORY;

